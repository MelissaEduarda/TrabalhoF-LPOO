    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        if(Dados.pesquisa(txtCpf.getText()).getSenha().equals(txtSenha.getText())){
            painelDetalhes.setText(String.valueOf(Dados.pesquisa(txtCpf.getText())));
            a = Dados.pesquisa(txtCpf.getText());
        }else{
            JOptionPane.showMessageDialog(null, "Usu치rio ou senha incorretos", "Alerta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed

